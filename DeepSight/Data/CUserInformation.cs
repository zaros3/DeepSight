using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace DeepSight
{
	public class CUserInformation : ICloneable
	{
		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// private
		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// 유저 아이디
		public string m_strID;
		// 유저 이름
		public string m_strName;
		// 패스워드 (인코딩)
		public string m_strPassword;
		// 권한 레벨
		public CDefine.enumUserAuthorityLevel m_eAuthorityLevel;

		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		//생성 : 
		//추가 : 
		//목적 : 생성자
		//설명 : 
		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		public CUserInformation()
		{
			m_strID = "Default";
			m_strName = "Default";
			m_strPassword = "";
			m_eAuthorityLevel = CDefine.enumUserAuthorityLevel.USER_AUTHORITY_LEVEL_FINAL;
		}

		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		//생성 : 
		//추가 : 
		//목적 : 생성자
		//설명 : 
		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		public CUserInformation( string strID, string strName, string strPassword, CDefine.enumUserAuthorityLevel eAuthorityLevel )
		{
			m_strID = strID;
			m_strName = strName;
			m_strPassword = strPassword;
			m_eAuthorityLevel = eAuthorityLevel;
		}

		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		//생성 : 
		//추가 : 
		//목적 : 복사
		//설명 : 
		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		public object Clone()
		{
			CUserInformation objUserInformation = new CUserInformation();
			objUserInformation.m_strID = this.m_strID;
			objUserInformation.m_strName = this.m_strName;
			objUserInformation.m_strPassword = this.m_strPassword;
			objUserInformation.m_eAuthorityLevel = this.m_eAuthorityLevel;
			return objUserInformation;
		}
	}
}